from AutoComments.async_proxy_pool.config import SERVER_HOST, SERVER_PORT
ipserver_addr = SERVER_HOST + ":" + str(SERVER_PORT)

minimum_ip_num = 100    # ip池中最少的ip个数

def get_ips():
	count = get_ip_num()
	print("ip pool has %d ips" % count)
	url = "http://%s/get/%d" % (ipserver_addr, count)
	html = requests.get(url).text
	ipsjson = json.loads(html, encoding='utf-8')
	ips = []
	for ipjson in ipsjson:
		for k, v in ipjson.items():
			if k == 'http':
				ips.append(v)
	return ips

def get_ip_num():
	print("================请检查 %s 服务是否已经启动， 若未启动请使用 main.py 来启动服务================="% ipserver_addr)
	html = requests.get("http://%s/count"% ipserver_addr).text
	countjson = json.loads(html, encoding='utf-8')
	return int(countjson['count'])


