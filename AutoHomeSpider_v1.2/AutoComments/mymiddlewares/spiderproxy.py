	@staticmethod
	def get_ips():
		urls = IpProxy.redis_conn.all_proxies()
		IpProxy.ips = [str(ip, encoding='utf-8') for ip in urls]
		return IpProxy.ips

	@staticmethod
	def get_ip():
		if len(IpProxy.ips) < IpProxy.minimum_ip_num:
			IpProxy.ips = IpProxy.get_ips()
		return IpProxy.ips[0]

	@staticmethod
	def remove_ip(ip):
		if ip in IpProxy.ips:
			IpProxy.ips.remove(ip)

	@staticmethod
	def has_ip(ip):
		return ip in IpProxy.ips

	@staticmethod
	def ips_num():
		return len(IpProxy.ips)

